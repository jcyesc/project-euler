/* Converts a string of digits to an array of n digits.
 *
 * @param s string that contains the digits.
 * @param numbers is the array where the digits will be stored.
 * @param size is the number of digits in the string and the size of the array.
 */
void convert_string_to_array_numbers(char *s, int numbers[], uint32_t size) {
  uint32_t i;

  for (i = 0; i < size; i++) {
      numbers[i] = s[i] - '0';
  }
}

/* Prints an array of numbers.
 *
 */
void print_array(int array[], uint32_t size) {
  uint32_t i = 0;

  for (i = 0; i < size; i++) {
    if (i % 50 == 0 && i > 0)
      printf("\n");

    printf("%u ", array[i]);
  }
}

/* Finds the greatest product of five consecutive digits in the array.
 *
 * Note: This algorithm can be improved. Instead of multiplying each time the five
 *     elements of the array, we divide the first element of the previous multiplication and
 *     we just multiply one more number each time. It's important to realize that if the number is
 *     Zero, we have to multiply the 5 digits.
 */
uint32_t find_greatest_product(int array[], uint32_t size) {
  uint32_t greates_product = 0;

  uint32_t i;
  uint32_t tmp;
  for (i = 0; i <= (size - 5); i++) {
    tmp = array[i] * array[i + 1] * array[i + 2] * array[i + 3] * array[i + 4];
    if (tmp > greates_product) {
      greates_product = tmp;
    }
  }

  return greates_product;
}


